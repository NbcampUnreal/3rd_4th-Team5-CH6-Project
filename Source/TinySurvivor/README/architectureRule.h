#pragma once
/**
 * ============================================================================
 * [Four Dolls] 아키텍처 설계 원칙 (Anti-God Object)
 * ============================================================================
 * 
 * 
 * * 1. Actor (The Container)
 * - 액터는 '컴포넌트들의 결합체' 역할만 수행한다.
 * - 직접적인 비즈니스 로직(계산, 상태 변경)을 최소화한다.
 * - 예: AFourDollsCharacter는 컴포넌트 간의 가교 역할만 하며, 700줄을 넘기지 않는다.
 * 
 * 
 * * 2. ActorComponent (The Specialist) - [객체 종속적 로직]
 * - 특정 액터에 종속되어 기능을 수행하는 '전문가'이다.
 * - 해당 액터의 '상태'를 직접 관리하고 로직을 처리한다.
 * - 예: USurvivalComponent (배고픔/목마름 관리), UInventoryComponent (아이템 관리)
 * - 규칙: 컴포넌트끼리는 서로의 존재를 최대한 모르게 설계한다 (느슨한 결합).
 * 
 * 
 * * 3. Subsystem (The Manager) - [전역/공통 로직]
 * - 특정 액터에 귀속되지 않는 게임 전체의 '서비스'를 담당한다.
 * - 데이터베이스 조회, 게임 설정, 전역 상태(낮/밤 등)를 관리한다.
 * - 예: UItemDataSubsystem (아이템 정보 조회), UWeatherSubsystem (날씨 시스템)
 * - 규칙: '어디서나 접근 가능'해야 하는 공유 기능은 컴포넌트가 아닌 서브시스템에 둔다.
 * 
 * 
 * * 4. Data Asset & Struct (The Blueprint) - [데이터의 분리]
 * - 하드코딩된 수치는 금지한다. 모든 수치는 Data Asset에서 가져온다.
 * - 로직이 없는 순수 데이터 덩어리는 별도의 Struct 파일로 분리한다.
 * 
 * 
 * * 5. Interface (The Contract) - [의존성 역전]
 * - 액터 간 직접 참조(Cast To...)를 피하기 위해 인터페이스를 적극 활용한다.
 * - 예: IInteractionInterface를 통해 상호작용 대상이 문인지, 상자인지 몰라도 실행 가능하게 한다.
 * 
 * 
 * ============================================================================
 */